    private void loadData(PreparedStatement executeThis){
        try{
            int CC;
            
            ResultSet Rs = executeThis.executeQuery();
            
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) tableOut.getModel();
            DFT.setRowCount(0);
            while (Rs.next()) {
                Vector v2 = new Vector();

                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getString("id_produk"));
                    v2.add(Rs.getString("nama_produk"));
                    v2.add(Rs.getString("batch_number"));
                    v2.add(Rs.getString("jumlah_kirim"));
                    v2.add(Rs.getString("tanggal_produksi"));
                    v2.add(Rs.getString("tanggal_kadaluwarsa"));
                    v2.add(Rs.getString("bahan_baku_utama"));
                    v2.add(Rs.getString("harga_per_kg"));
                    v2.add(Rs.getString("tanggal_dokumen_kontrak"));
                    v2.add(Rs.getString("pembeli"));
                    v2.add(Rs.getString("sisa_kontrak"));
                }
                DFT.addRow(v2);
            }
            resetText();
            buttonScenario("buka");
            textEnabled(true);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this,"Error: " + e);
        }
    }
       
