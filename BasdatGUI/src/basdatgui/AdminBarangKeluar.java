    private void bGetInvoiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGetInvoiceActionPerformed
        int index = tableOut.getSelectedRow();
	String ID = tableOut.getValueAt(index, 0).toString();
	try{
            String filePath = "src/basdatgui/invoice.jrxml";
            JasperDesign jasperDesign =  JRXmlLoader.load(new FileInputStream(new File(filePath)));
            JRDesignQuery newQuery = new JRDesignQuery();
	
            String sql = "SELECT "
                        + "pembeli AS nama_customer,"
                        + "Tanggal_dokumen_kontrak AS tanggal_pemesanan,"
                        + "nama_produk,"
                        + "id_produk,"
                        + "tanggal_produksi,"
                        + "tanggal_kadaluwarsa,"
                        + "jumlah_kirim AS jumlah_pesanan,"
                        + "harga_per_kg,"
                        + "(jumlah_kirim * harga_per_kg) AS total "
                        + "FROM produk_keluar "
                        + "WHERE id_produk = " + ID + "";
            newQuery.setText(sql);
            jasperDesign.setQuery(newQuery);
		
            JasperReport jasperReport = JasperCompileManager.compileReport(jasperDesign);
            
            Map<String, Object> paramtr = new HashMap<>();
            paramtr.put("searchFor", ID);
		
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, paramtr, getUrl());
            JasperViewer.viewReport(jasperPrint, false);
            
            String PATH = "uploads/Receipts/";
            File directory = new File(PATH);
            if (!directory.exists()) {
            	directory.mkdirs();
            	// use directory.mkdirs(); here instead.
            	// If you require it to make the entire directory path including parents,
            }
            try (OutputStream os = new FileOutputStream(new File("uploads/Receipts/Seller Reciept-OrderID" + ID + ".pdf"))) {
                JasperExportManager.exportReportToPdfStream(jasperPrint, os);
                JasperViewer.viewReport(jasperPrint, false);
            }
	} catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error! Hubungi Admin!");
            e.printStackTrace();
	}        
    }//GEN-LAST:event_bGetInvoiceActionPerformed

